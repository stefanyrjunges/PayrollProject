    public void update() {

        HoursSubmissionData submissionData = submissionManager.loadStatus();

        if (submissionData.isPending()) {
            empNameTF.setText(submissionData.getEmployeeName());
            empIdTF.setText(String.valueOf(submissionData.getEmployeeId()));
            warningLBL.setText("You have pending submissions to review for Week " + submissionData.getWeekNumber());
            hoursWorkedTF.setText(String.valueOf(submissionData.getTotalHours()));
            estSalaryTF.setText(String.valueOf(submissionData.getSalary()));
            daysWorkedTF.setText(submissionData.getWorkedDays());

            empIdTF.setVisible(true);
            hoursWorkedTF.setVisible(true);
            estSalaryTF.setVisible(true);
            daysWorkedTF.setVisible(true);
            reasonTA.setText(" ");

        } else {
            warningLBL.setText("No pending approvals.");
            empNameTF.setVisible(false);
            empNameLBL.setVisible(false);
            empIdTF.setVisible(false);
            empIdLBL.setVisible(false);
            hoursWorkedTF.setVisible(false);
            hoursWorkedLBL.setVisible(false);
            estSalaryTF.setVisible(false);
            estSalaryLBL.setVisible(false);
            daysWorkedTF.setVisible(false);
            daysWorkedLBL.setVisible(false);
            approveBTN.setVisible(false);
            rejectBTN.setVisible(false);
        }
    }

    public void approveHours() {
        if (!empIdTF.getText().isEmpty()) {
            int employeeId = Integer.parseInt(empIdTF.getText());
            submissionManager.approveHours(employeeId);
            update();
        }
    }

    public void rejectHours() {
        if (!empIdTF.getText().isEmpty()) {
            int employeeId = Integer.parseInt(empIdTF.getText());
            submissionManager.rejectHours(employeeId, reasonTA.getText());
            update();
        }
    }

